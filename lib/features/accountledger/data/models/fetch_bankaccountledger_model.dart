class FetchBankAccountLedgerResponseModel
    extends FetchBankAccountLedgerResponseEntity {
  FetchBankAccountLedgerResponseModel({
    super.status,
    super.error,
    super.message,
    List<FetchBankAccountLedgerDetailsModel>? data,
  }) : super(data: data);

  factory FetchBankAccountLedgerResponseModel.fromJson(
    Map<String, dynamic> json,
  ) {
    return FetchBankAccountLedgerResponseModel(
      status: json['status'],
      error: json['error'],
      message: json['message'],
      data: json['data'] != null
          ? List<FetchBankAccountLedgerDetailsModel>.from(
              json['data'].map(
                (x) => FetchBankAccountLedgerDetailsModel.fromJson(x),
              ),
            )
          : [],
    );
  }

  Map<String, dynamic> toJson() => {
    "status": status,
    "error": error,
    "message": message,
    "data": data
        ?.map((x) => (x as FetchBankAccountLedgerDetailsModel).toJson())
        .toList(),
  };
}

